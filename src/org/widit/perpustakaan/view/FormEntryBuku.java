    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String judul = txtJudul.getText().trim();
    String thnTerbit = txtThnTerbit.getText().trim();
    String penulis = txtPenulis.getText().trim();
    String penerbit = txtPenerbit.getText().trim();
    String hrgSewa = txtHrgSewa.getText().trim();
    
    boolean isValid = true;

    if (judul.isEmpty()) {
        lblValidJudul.setText("Judul Harus Diisi.");
        isValid = false;
    } else {
        lblValidJudul.setText("");
    }

    if (thnTerbit.isEmpty()) {
        txtValidThnTerbit.setText("Tahun Terbit Harus Diisi.");
        isValid = false;
    } else {
        txtValidThnTerbit.setText("");
    }

    if (penulis.isEmpty()) {
        lblValidPenulis.setText("Penulis Harus Diisi.");
        isValid = false;
    } else {
        lblValidPenulis.setText("");
    }

    if (penerbit.isEmpty()) {
        lblValidPenerbit.setText("Penerbit Harus Diisi.");
        isValid = false;
    } else {
        lblValidPenerbit.setText("");
    }

    if (hrgSewa.isEmpty()) {
        txtValidHarga.setText("Harga Sewa Harus Diisi.");
        isValid = false;
    } else {
        txtValidHarga.setText("");
    }

    if (isValid) {
        jLabel6.setText("Berhasil Submit");
    } else {
        jLabel6.setText("Silakan Isi Semua Field.");
    }
    
    }//GEN-LAST:event_jButton1ActionPerformed

