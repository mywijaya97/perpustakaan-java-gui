    private void txtThnTerbitKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtThnTerbitKeyReleased
        try {
            long d = Long.parseLong(this.txtThnTerbit.getText());
            if ( d < 0 ){
                throw new NumberFormatException("Tidak Boleh Negatif");
            }

            this.txtValidThnTerbit.setText("");
        } catch (NumberFormatException e) {
            this.txtValidThnTerbit.setText("Inputan Harus Angka dan Tidak Boleh Negatif");
        }
    }//GEN-LAST:event_txtThnTerbitKeyReleased

    private void txtPenulisKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPenulisKeyReleased
        String input = this.txtPenulis.getText();
        if (input.matches("[a-zA-Z]+")) { // Hanya huruf
            this.lblValidPenulis.setText("");
        } else {
            this.lblValidPenulis.setText("Inputan Harus Huruf dan Tidak Boleh Angka");
        }
    }//GEN-LAST:event_txtPenulisKeyReleased

